package structural;

public class Facade {

	/*
	 * Let say FACADE is office front-desk or RECEPTIONIST
	 * 
	 * Facade design pattern provide a unified interface to a set of interfaces in a subsystem. 
	 * Facade defines a higher-level interface that makes the subsystem easier to use.
	 * 
	 * Example: https://howtodoinjava.com/design-patterns/structural/facade-design-pattern/
	 * 
	 * When we have to start a computer, all we have to do is press the start button. 
	 * We really do not care what all things go inside the computer hardware and software.
	 * 
	 * We simply call the method dataSource.getConnection() to get the connection but internally a lot of things 
	 * happen such as loading the driver, creating connection or fetching connection from pool, update stats and then 
	 * return the connection reference to caller method. 
	 */
}
